num1 = 3.1425467389
num2 = 10.2903948

# PREVIOUS
print('num1 is', num1, 'and num2 is', num2)
# num1 is 3.1425467389 and num2 is 10.2903948 


# FORMAT METHOD
print('num 1 is {0} and num 2 is {1}'.format(num1,num2))
# num 1 is 3.1425467389 and num 2 is 10.2903948

print('num 1 is {0:.2} and num 2 is {1:.3}'.format(num1,num2))
# num 1 is 3.1 and num 2 is 10.3


# USING F-STRINGS
print(f'num1 is {num1:.3f} and num 2 is {num2:.3f}')
# num1 is 3.143 and num 2 is 10.290


