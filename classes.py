# how to find out the type of a variable

name = 'gisel'
age = 33
nums = [1,2,11,22,3,4,8]

# just use type(theVariableName)

print(type(name)) #<class 'str'>
print(type(age)) #<class 'int'>
print(type(nums)) #<class 'list'>

